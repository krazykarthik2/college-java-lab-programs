import java.util.*;
import java.lang.*;
class Arith{
    public static void main(String[] args){
        Scanner scan = new Scanner(System.in);
        int x = scan.nextInt();
        int y = scan.nextInt();
        System.out.println("x+y="+(x+y));
        System.out.println("x-y="+(x-y));
        System.out.println("x*y="+(x*y));
        System.out.println("x/y="+(x/y));
        System.out.println("x%y="+(x%y));
    }
    
}
